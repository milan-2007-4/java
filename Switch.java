//example of java switch statement 
class Switch
{
	public static void main(String[] args)
	{
		String day="wed";
		switch(day)
		{
			case "mon":
			System.out.println("monday");
			break;

			case "tue":
			System.out.println("tuesday");
			break;
		
			case "wed":
			System.out.println("wensday");
			break;
		
			case "thu":
			System.out.println("thusday");
			break;
		}
	}
}