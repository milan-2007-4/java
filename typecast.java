//widening type casting, implycit type casting, automatically type casting
class typecast
{
	public static void main(String[] args)
	{
		int i=10;
		long l=i;
		double d=l;
		System.out.println("integer is = "+i);
		System.out.println("long is = "+l);
		System.out.println("double is = "+d);
	}
}